public class hm_05 {
    public static void main(String[] args) {
        int a=12;
        int b=7;
        
        int sum=a+b;
        int difference=a-b;
        int product=a*b;
        int quotient=a/b;
        int remainder=a%b; 

        System.out.println("和: " + sum);
        System.out.println("差: " + difference);
        System.out.println("積: " + product);
        System.out.println("商: " + quotient);
        System.out.println("餘: "+remainder);

    }
}
